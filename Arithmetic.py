a=10
b=20
# Addition(+): Operand1 + Operand2 = Result
print(a+b)
print(538+536)
print("Nagendra"+"Babu")
# subtraction(-): Operand1 - Operand2 = Result
print(a-b)
print(15-9)
# Multiplication(*): Operand1 * Operand2 = Result
print(a*b)
print(15 * 9)
print("Naga"*3)
# Division(/) : Operand1 / Operand2 = Result
print(a/b)
print(77/6)
# Modulus (%) : Operand1 % Operand2 = Result
print(a%b)
print(538%20)
# Exponential (**): Operand1 ** Operand2 = Result
print(a**b)
print(21**4)
# Floor Division (//): Operand1 // Operand2 = Result
print(a//b)

